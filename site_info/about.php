<h2>About LabLoGGr</h2>
  <p>Efficient inventory management is essential for optimizing a laboratory. Knowing what materials you have, their quantities, and where they are stored not only improves storage efficiency but also streamlines workflows by eliminating time wasted searching for materials. 
    In large laboratories with many collaborators, such as those in universities, the need for a clear structure becomes evident. Irregular users, such as students, often struggle to locate the necessary materials to continue their work, leading to workflow bottlenecks and increased 
    waiting times to seek assistance from responsible teacher assistans (TAs). Without a defined system for where items are stored and should be returned, the lab can quickly descend into chaos, hindering productivity.
  </p>
  <p>This need is what inspired us to create LabLoGGr. The goal was to develop a software solution for managing laboratory inventory systems in universities, offering easy and customized access tailored to the needs of different types of users. 
    The core functionality of the system will be a digital visualization of laboratory inventory. Since university faculty are assigned to specific labs and storage areas, each member of staff (Teachers and TAs) will have a personal account, 
    granting them access only to the laboratories and storage rooms they oversee and work in. 
  </p>
  <p>Both teachers and TAs will be able to view the inventory of their assigned locations, as well as update item quantities, add new items, or remove existing ones. Access management will be overseen by an admin, who will have the ability to assign access to specific users, 
    create new laboratories or storage rooms, and delete existing ones. Students, on the other hand, will only have access to laboratory inventory during designated courses. Staff members will be able to create “course keys”, linking specific labs to students, who will then be 
    able to view a filtered version of the inventory, displaying only the materials relevant to their coursework. 
  </p>
<h2>WHO ARE WE?</h2>
  <p>We are a team of six passionate students dedicated to transforming laboratory management in university settings. Our journey began with a simple observation: laboratory inefficiency often stems from the chaos of misplaced materials, lack of structured storage, and the struggles of students and 
    faculty navigating complex inventories. In large university labs, where numerous collaborators work side-by-side, optimizing inventory management can significantly boost productivity and streamline workflows.</p>
  <p>By providing a structured and user-friendly system, we aim to eliminate bottlenecks, enhance collaboration, and bring clarity to university laboratory environments. LabLoGGr isn’t just about software; it’s about making research and learning more efficient, one lab at a time.</p>
